keith({
    pattern: "thunder",
    desc: "Generate a logo with your text",
    category: "Logo",
    react: "üí´",
    filename: __filename
}, async (context) => {
    try {
        const { client, m, fetchLogoUrl, text, args, quoted, botname, reply } = context;

        if (!args || !text) {
            return reply("_Please provide text to create logo");
        }

        const logoUrl = await fetchLogoUrl(
            "https://en.ephoto360.com/thunder-text-effect-online-97.html",
            text
        );

        if (logoUrl) {
            await client.sendMessage(m.chat, {
                image: { url: logoUrl },
                caption: ` logo generated by ${botname}`
            }, { quoted });
        } else {
            reply("_Unable to fetch logo. Please try again later._");
        }

    } catch (error) {
        console.error(" logo command error:", error);
        context.reply(`‚ùå An error occurred:\n${error.message}`);
    }
});
